package demo;

import com.ibm.sbt.playground.snippets.CategoryNode;


/**
 * Definition of a code snippet.
 */
public class DemoSnippetCategory extends CategoryNode {

	private String path;
	
	public DemoSnippetCategory(CategoryNode parent, String name, String path) {
		super(parent,name);
		this.path = path;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}	
	
}
